package dailydairy;

import java.util.Scanner;

public class FirstNevenNumbers1 {
	public static void main(String []args)
	{
		Scanner sc=new Scanner(System.in);
		int n=sc.nextInt();
		evenNumbers(n);
	}
	static void evenNumbers(int k)
	{
		for(int i=1;i<=k;i++)
		{
			System.out.print((2*i)+" ");
		}
	}

}
